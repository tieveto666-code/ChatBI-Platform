from enum import IntEnum


class ErrorCode(IntEnum):
    SUCCESS = 0

    # 认证错误 (1000-1999)
    TOKEN_EXPIRED = 1001
    INVALID_CREDENTIALS = 1002
    TOKEN_INVALID = 1003
    REGISTRATION_FAILED = 1004
    USERNAME_EXISTS = 1005

    # 参数错误 (2000-2999)
    VALIDATION_ERROR = 2001
    MISSING_FIELD = 2002

    # 业务错误 (3000-3999)
    SQL_EXECUTION_ERROR = 3001
    LLM_CALL_FAILED = 3002
    FILE_TOO_LARGE = 3003
    FILE_PARSE_ERROR = 3004
    UNSUPPORTED_FILE_TYPE = 3005
    MEMORY_LIMIT_EXCEEDED = 3006

    # 权限错误 (4000-4999)
    FORBIDDEN = 4001
    INSUFFICIENT_PERMISSION = 4002

    # 系统错误 (5000-5999)
    INTERNAL_ERROR = 5001
    DATABASE_ERROR = 5002
    LLM_PROVIDER_NOT_FOUND = 5003
